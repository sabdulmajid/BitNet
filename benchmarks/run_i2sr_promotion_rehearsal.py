#!/usr/bin/env python3
"""Rehearse I2_SR promotion using split root and llama.cpp-submodule patches."""

from __future__ import annotations

import argparse
import os
import subprocess
from pathlib import Path


DEFAULT_TARGETS = ["llama-cli", "llama-bench", "llama-perplexity", "llama-quantize"]


def run(command: list[str], *, cwd: Path, check: bool = True) -> subprocess.CompletedProcess[str]:
    print("+ " + " ".join(command), flush=True)
    return subprocess.run(command, cwd=cwd, text=True, check=check)


def probe(command: list[str], *, cwd: Path) -> bool:
    completed = subprocess.run(command, cwd=cwd, text=True, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL, check=False)
    return completed.returncode == 0


def cmake_build(build_dir: Path, targets: list[str], jobs: int, *, cwd: Path) -> None:
    command = ["cmake", "--build", str(build_dir)]
    if targets:
        command.extend(["--target", *targets])
    command.extend(["-j", str(jobs)])
    run(command, cwd=cwd)


def apply_check(patch: Path, *, cwd: Path) -> bool:
    return probe(["git", "apply", "--check", str(patch)], cwd=cwd)


def reverse_check(patch: Path, *, cwd: Path) -> bool:
    return probe(["git", "apply", "--reverse", "--check", str(patch)], cwd=cwd)


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--repo-root", type=Path, default=Path.cwd())
    parser.add_argument("--submodule-dir", type=Path, default=Path("3rdparty/llama.cpp"))
    parser.add_argument("--root-patch", type=Path, default=Path("patches/bitnet-i2sr-root-runtime.patch"))
    parser.add_argument("--submodule-patch", type=Path, default=Path("patches/llama-i2sr-row-scale-qtype.submodule.patch"))
    parser.add_argument("--build-dir", type=Path, default=Path("build-portable-avx2"))
    parser.add_argument("--target", action="append", default=None)
    parser.add_argument("--jobs", type=int, default=max(1, min(12, os.cpu_count() or 1)))
    parser.add_argument("--skip-build", action="store_true")
    parser.add_argument("--restore-build", action="store_true")
    parser.add_argument(
        "--output-json",
        type=Path,
        default=Path("benchmark_results/row_scale_qtype_productization_gate_i2sr_promotion_rehearsal_2026-05-13.json"),
    )
    parser.add_argument(
        "--output-md",
        type=Path,
        default=Path("benchmarks/results/row_scale_qtype_productization_gate_i2sr_promotion_rehearsal_2026-05-13.md"),
    )
    args = parser.parse_args()

    root = args.repo_root.resolve()
    submodule = args.submodule_dir if args.submodule_dir.is_absolute() else root / args.submodule_dir
    root_patch = args.root_patch if args.root_patch.is_absolute() else root / args.root_patch
    submodule_patch = args.submodule_patch if args.submodule_patch.is_absolute() else root / args.submodule_patch
    build_dir = args.build_dir if args.build_dir.is_absolute() else root / args.build_dir
    output_json = args.output_json if args.output_json.is_absolute() else root / args.output_json
    output_md = args.output_md if args.output_md.is_absolute() else root / args.output_md
    targets = args.target or DEFAULT_TARGETS
    build_note = (
        f"patched build targets {','.join(targets)} were rebuilt"
        if not args.skip_build
        else "patched build was skipped"
    )
    restore_note = (
        f"default build targets {','.join(targets)} were rebuilt after patch reversal"
        if args.restore_build
        else "default build was not rebuilt after patch reversal"
    )

    if not root_patch.exists():
        raise SystemExit(f"missing root patch: {root_patch}")
    if not submodule_patch.exists():
        raise SystemExit(f"missing submodule patch: {submodule_patch}")
    if reverse_check(root_patch, cwd=root) or reverse_check(submodule_patch, cwd=submodule):
        raise SystemExit("one or more split patches already appears to be applied")
    if not apply_check(root_patch, cwd=root):
        raise SystemExit(f"root patch does not apply cleanly: {root_patch}")
    if not apply_check(submodule_patch, cwd=submodule):
        raise SystemExit(f"submodule patch does not apply cleanly: {submodule_patch}")

    root_applied = False
    submodule_applied = False
    try:
        run(["git", "apply", str(root_patch)], cwd=root)
        root_applied = True
        run(["git", "apply", str(submodule_patch)], cwd=submodule)
        submodule_applied = True
        if not args.skip_build:
            cmake_build(build_dir, targets, args.jobs, cwd=root)
        run(
            [
                "python",
                "benchmarks/audit_row_scale_qtype_productization.py",
                "--scenario-label",
                "I2_SR split-patch promotion rehearsal",
                "--scenario-note",
                (
                    "Generated by benchmarks/run_i2sr_promotion_rehearsal.py after applying "
                    "patches/bitnet-i2sr-root-runtime.patch in the superproject and "
                    "patches/llama-i2sr-row-scale-qtype.submodule.patch inside 3rdparty/llama.cpp; "
                    f"{build_note}; the script reverses both patches before exiting; {restore_note}."
                ),
                "--output-json",
                str(output_json),
                "--output-md",
                str(output_md),
            ],
            cwd=root,
        )
    finally:
        if submodule_applied and reverse_check(submodule_patch, cwd=submodule):
            run(["git", "apply", "--reverse", str(submodule_patch)], cwd=submodule)
        if root_applied and reverse_check(root_patch, cwd=root):
            run(["git", "apply", "--reverse", str(root_patch)], cwd=root)
        if not apply_check(root_patch, cwd=root):
            raise SystemExit("root patch was not cleanly reversed")
        if not apply_check(submodule_patch, cwd=submodule):
            raise SystemExit("submodule patch was not cleanly reversed")
        if args.restore_build:
            cmake_build(build_dir, targets, args.jobs, cwd=root)

    print(f"Wrote {output_json}")
    print(f"Wrote {output_md}")


if __name__ == "__main__":
    main()
