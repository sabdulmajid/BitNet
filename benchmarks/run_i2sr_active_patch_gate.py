#!/usr/bin/env python3
"""Apply the I2_SR runtime patch, run the productization gate, then restore source."""

from __future__ import annotations

import argparse
import os
import subprocess
from pathlib import Path


def run(command: list[str], *, cwd: Path, check: bool = True) -> subprocess.CompletedProcess[str]:
    print("+ " + " ".join(command), flush=True)
    return subprocess.run(command, cwd=cwd, text=True, check=check)


def probe(command: list[str], *, cwd: Path) -> bool:
    completed = subprocess.run(command, cwd=cwd, text=True, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL, check=False)
    return completed.returncode == 0


def cmake_build(build_dir: Path, targets: list[str], jobs: int, *, cwd: Path) -> None:
    command = ["cmake", "--build", str(build_dir)]
    if targets:
        command.extend(["--target", *targets])
    command.extend(["-j", str(jobs)])
    run(command, cwd=cwd)


def patch_is_applied(root: Path, patch: Path) -> bool:
    return probe(["git", "apply", "--reverse", "--check", str(patch)], cwd=root)


def patch_can_apply(root: Path, patch: Path) -> bool:
    return probe(["git", "apply", "--check", str(patch)], cwd=root)


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--repo-root", type=Path, default=Path.cwd())
    parser.add_argument("--patch", type=Path, default=Path("patches/llama-i2sr-row-scale-qtype.patch"))
    parser.add_argument("--build-dir", type=Path, default=Path("build-portable-avx2"))
    parser.add_argument(
        "--target",
        action="append",
        default=None,
        help="CMake target to build; repeatable. Defaults to runtime and quantizer targets.",
    )
    parser.add_argument("--jobs", type=int, default=max(1, min(12, os.cpu_count() or 1)))
    parser.add_argument("--skip-build", action="store_true")
    parser.add_argument(
        "--restore-build",
        action="store_true",
        help="After reversing the patch, rebuild the same targets so build artifacts match the unpatched source.",
    )
    parser.add_argument("--output-json", type=Path, default=Path("benchmark_results/row_scale_qtype_productization_gate_i2sr_active_patch_2026-05-13.json"))
    parser.add_argument("--output-md", type=Path, default=Path("benchmarks/results/row_scale_qtype_productization_gate_i2sr_active_patch_2026-05-13.md"))
    args = parser.parse_args()

    root = args.repo_root.resolve()
    patch = args.patch if args.patch.is_absolute() else root / args.patch
    build_dir = args.build_dir if args.build_dir.is_absolute() else root / args.build_dir
    output_json = args.output_json if args.output_json.is_absolute() else root / args.output_json
    output_md = args.output_md if args.output_md.is_absolute() else root / args.output_md
    targets = args.target or ["llama-cli", "llama-bench", "llama-perplexity", "llama-quantize"]

    if not patch.exists():
        raise SystemExit(f"missing patch: {patch}")
    if patch_is_applied(root, patch):
        raise SystemExit(f"patch is already applied: {patch}")
    if not patch_can_apply(root, patch):
        raise SystemExit(f"patch does not apply cleanly: {patch}")

    applied = False
    try:
        run(["git", "apply", str(patch)], cwd=root)
        applied = True
        if not args.skip_build:
            cmake_build(build_dir, targets, args.jobs, cwd=root)
        run(
            [
                "python",
                "benchmarks/audit_row_scale_qtype_productization.py",
                "--scenario-label",
                "I2_SR candidate patch applied",
                "--scenario-note",
                (
                    "Generated by benchmarks/run_i2sr_active_patch_gate.py after applying "
                    "patches/llama-i2sr-row-scale-qtype.patch to the clean active source tree; "
                    "the script reverses the patch before exiting."
                ),
                "--output-json",
                str(output_json),
                "--output-md",
                str(output_md),
            ],
            cwd=root,
        )
    finally:
        if applied:
            if patch_is_applied(root, patch):
                run(["git", "apply", "--reverse", str(patch)], cwd=root)
            if not patch_can_apply(root, patch):
                raise SystemExit("patch was not cleanly reversed")
            if args.restore_build:
                cmake_build(build_dir, targets, args.jobs, cwd=root)

    if not patch_can_apply(root, patch):
        raise SystemExit("patch was not cleanly reversed")
    print(f"Wrote {output_json}")
    print(f"Wrote {output_md}")


if __name__ == "__main__":
    main()
